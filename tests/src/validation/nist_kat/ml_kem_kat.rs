//! ML-KEM Deterministic Keygen Regression Tests
//!
//! These tests use `keygen_from_seed(d, z)` from the `fips203` crate to generate
//! keypairs deterministically, then verify the output matches recorded fingerprints.
//! This catches implementation regressions (algorithm changes, dependency updates)
//! that silently alter keygen output.
//!
//! Additionally, each test verifies encaps/decaps roundtrip correctness — shared
//! secrets must match after a full encapsulate → decapsulate cycle.
//!
//! ## Why not full NIST ACVP vectors?
//!
//! NIST ACVP test vectors require matching the exact internal randomness injection
//! path (d, z for keygen; m for encaps). The `fips203` crate exposes
//! `keygen_from_seed` but encaps is still randomized internally, so we cannot
//! produce deterministic ciphertexts or shared secrets for comparison. The keygen
//! fingerprint + roundtrip approach provides strong regression coverage.
//!
//! ## Security Levels
//! - ML-KEM-512: NIST Security Level 1 (128-bit classical, quantum-safe)
//! - ML-KEM-768: NIST Security Level 3 (192-bit classical, quantum-safe)
//! - ML-KEM-1024: NIST Security Level 5 (256-bit classical, quantum-safe)

use super::{NistKatError, decode_hex};
use fips203::ml_kem_512;
use fips203::ml_kem_768;
use fips203::ml_kem_1024;
use fips203::traits::{Decaps, Encaps, KeyGen, SerDes};

/// Keygen fingerprint: first 32 bytes of ek and dk for a known seed.
pub struct MlKemKeygenFingerprint {
    pub test_name: &'static str,
    pub d: [u8; 32],
    pub z: [u8; 32],
    /// First 32 bytes of the encapsulation key (hex).
    pub ek_first32: &'static str,
    /// First 32 bytes of the decapsulation key (hex).
    pub dk_first32: &'static str,
    /// Expected encapsulation key length in bytes.
    pub ek_len: usize,
    /// Expected decapsulation key length in bytes.
    pub dk_len: usize,
}

fn hex(b: &[u8]) -> String {
    b.iter().map(|x| format!("{:02x}", x)).collect()
}

