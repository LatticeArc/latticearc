/// Run ML-KEM-512 keygen regression + roundtrip test.
///
/// # Errors
/// Returns `NistKatError` if keygen fingerprint mismatches or roundtrip fails.
pub fn run_ml_kem_512_kat() -> Result<(), NistKatError> {
    for fp in ML_KEM_512_FINGERPRINTS {
        let expected_ek_prefix = decode_hex(fp.ek_first32)?;
        let expected_dk_prefix = decode_hex(fp.dk_first32)?;

        let (ek, dk) = <ml_kem_512::KG as KeyGen>::keygen_from_seed(fp.d, fp.z);
        let ek_bytes = ek.into_bytes();
        let dk_bytes = dk.into_bytes();

        // Verify key sizes
        if ek_bytes.len() != fp.ek_len {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-512".to_string(),
                test_name: fp.test_name.to_string(),
                message: format!("ek length: expected {}, got {}", fp.ek_len, ek_bytes.len()),
            });
        }
        if dk_bytes.len() != fp.dk_len {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-512".to_string(),
                test_name: fp.test_name.to_string(),
                message: format!("dk length: expected {}, got {}", fp.dk_len, dk_bytes.len()),
            });
        }

        // Verify keygen fingerprints
        if ek_bytes[..32] != expected_ek_prefix[..] {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-512".to_string(),
                test_name: fp.test_name.to_string(),
                message: format!(
                    "ek prefix mismatch: expected {}, got {}",
                    fp.ek_first32,
                    hex(&ek_bytes[..32])
                ),
            });
        }
        if dk_bytes[..32] != expected_dk_prefix[..] {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-512".to_string(),
                test_name: fp.test_name.to_string(),
                message: format!(
                    "dk prefix mismatch: expected {}, got {}",
                    fp.dk_first32,
                    hex(&dk_bytes[..32])
                ),
            });
        }

        // Roundtrip: encaps → decaps → shared secrets must match
        let ek_restored = ml_kem_512::EncapsKey::try_from_bytes(ek_bytes)
            .map_err(|e| NistKatError::ImplementationError(format!("ek deser: {:?}", e)))?;
        let dk_restored = ml_kem_512::DecapsKey::try_from_bytes(dk_bytes)
            .map_err(|e| NistKatError::ImplementationError(format!("dk deser: {:?}", e)))?;

        let (ss_sender, ct) = ek_restored
            .try_encaps()
            .map_err(|e| NistKatError::ImplementationError(format!("encaps: {:?}", e)))?;
        let ss_receiver = dk_restored
            .try_decaps(&ct)
            .map_err(|e| NistKatError::ImplementationError(format!("decaps: {:?}", e)))?;

        if ss_sender != ss_receiver {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-512".to_string(),
                test_name: fp.test_name.to_string(),
                message: "Shared secrets do not match after encaps/decaps".to_string(),
            });
        }
    }
    Ok(())
}

/// Run ML-KEM-768 keygen regression + roundtrip test.
///
/// # Errors
/// Returns `NistKatError` if keygen fingerprint mismatches or roundtrip fails.
pub fn run_ml_kem_768_kat() -> Result<(), NistKatError> {
    for fp in ML_KEM_768_FINGERPRINTS {
        let expected_ek_prefix = decode_hex(fp.ek_first32)?;
        let expected_dk_prefix = decode_hex(fp.dk_first32)?;

        let (ek, dk) = <ml_kem_768::KG as KeyGen>::keygen_from_seed(fp.d, fp.z);
        let ek_bytes = ek.into_bytes();
        let dk_bytes = dk.into_bytes();

        if ek_bytes.len() != fp.ek_len || dk_bytes.len() != fp.dk_len {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-768".to_string(),
                test_name: fp.test_name.to_string(),
                message: "Key length mismatch".to_string(),
            });
        }
        if ek_bytes[..32] != expected_ek_prefix[..] || dk_bytes[..32] != expected_dk_prefix[..] {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-768".to_string(),
                test_name: fp.test_name.to_string(),
                message: format!(
                    "keygen fingerprint mismatch (ek: {}, dk: {})",
                    hex(&ek_bytes[..32]),
                    hex(&dk_bytes[..32])
                ),
            });
        }

        let ek_r = ml_kem_768::EncapsKey::try_from_bytes(ek_bytes)
            .map_err(|e| NistKatError::ImplementationError(format!("{:?}", e)))?;
        let dk_r = ml_kem_768::DecapsKey::try_from_bytes(dk_bytes)
            .map_err(|e| NistKatError::ImplementationError(format!("{:?}", e)))?;
        let (ss1, ct) =
            ek_r.try_encaps().map_err(|e| NistKatError::ImplementationError(format!("{:?}", e)))?;
        let ss2 = dk_r
            .try_decaps(&ct)
            .map_err(|e| NistKatError::ImplementationError(format!("{:?}", e)))?;
        if ss1 != ss2 {
            return Err(NistKatError::TestFailed {
                algorithm: "ML-KEM-768".to_string(),
                test_name: fp.test_name.to_string(),
                message: "Shared secrets mismatch".to_string(),
            });
        }
    }
    Ok(())
}

