//! ML-DSA Keygen Regression + Sign/Verify Roundtrip Tests
//!
//! These tests use `try_keygen_with_rng` from the `fips204` crate with a
//! deterministic CSPRNG to generate keypairs, then verify:
//! 1. Keygen output matches recorded fingerprints (regression detection)
//! 2. Sign/verify roundtrip works correctly for each parameter set
//!
//! ## Why not full NIST ACVP vectors?
//!
//! ML-DSA signing (FIPS 204 Algorithm 7) uses internal randomness (rnd).
//! The `fips204` crate does not expose a `sign_with_rng` that lets us
//! inject test randomness, so signatures are non-deterministic. We verify
//! keygen determinism (via `try_keygen_with_rng`) and sign/verify
//! correctness (roundtrip), but cannot compare against a fixed expected
//! signature.
//!
//! ## Security Levels
//! - ML-DSA-44: NIST Security Level 2 (128-bit classical security)
//! - ML-DSA-65: NIST Security Level 3 (192-bit classical security)
//! - ML-DSA-87: NIST Security Level 5 (256-bit classical security)

use super::{NistKatError, decode_hex};
use fips204::ml_dsa_44;
use fips204::ml_dsa_65;
use fips204::ml_dsa_87;
use fips204::traits::{KeyGen, SerDes, Signer, Verifier};

/// Keygen fingerprint for ML-DSA.
pub struct MlDsaKeygenFingerprint {
    pub test_name: &'static str,
    /// First 32 bytes of the public key (hex), generated from a zero-seed RNG.
    pub pk_first32: &'static str,
    /// Expected public key length.
    pub pk_len: usize,
}

fn hex(b: &[u8]) -> String {
    b.iter().map(|x| format!("{:02x}", x)).collect()
}

/// Deterministic RNG that cycles the seed bytes.
/// Used ONLY for keygen regression tests — not for production crypto.
///
/// `fips204 0.4.x` uses `rand_core 0.6` traits (its `try_keygen_with_rng`
/// signature: `<R: CryptoRng + RngCore>` where both are from `rand_core 0.6`).
/// We implement those traits via the `rand_core_0_6` workspace alias rather
/// than `rand_core 0.9` so the type satisfies `fips204`'s bound.
struct SeedRng([u8; 32]);

impl rand_core_0_6::RngCore for SeedRng {
    fn next_u32(&mut self) -> u32 {
        0
    }
    fn next_u64(&mut self) -> u64 {
        0
    }
    fn fill_bytes(&mut self, dest: &mut [u8]) {
        for (d, s) in dest.iter_mut().zip(self.0.iter().cycle()) {
            *d = *s;
        }
    }
    fn try_fill_bytes(&mut self, dest: &mut [u8]) -> Result<(), rand_core_0_6::Error> {
        self.fill_bytes(dest);
        Ok(())
    }
}

impl rand_core_0_6::CryptoRng for SeedRng {}

