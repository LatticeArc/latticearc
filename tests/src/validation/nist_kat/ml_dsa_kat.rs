/// Run ML-DSA-44 keygen regression + sign/verify roundtrip.
///
/// # Errors
/// Returns `NistKatError` if keygen fingerprint mismatches or sign/verify fails.
pub fn run_ml_dsa_44_kat() -> Result<(), NistKatError> {
    let fp = &ML_DSA_44_FINGERPRINT;
    let expected_pk_prefix = decode_hex(fp.pk_first32)?;

    let (pk, sk) = ml_dsa_44::try_keygen_with_rng(&mut SeedRng([0u8; 32]))
        .map_err(|e| NistKatError::ImplementationError(format!("keygen: {:?}", e)))?;

    let pk_bytes = pk.into_bytes();
    if pk_bytes.len() != fp.pk_len {
        return Err(NistKatError::TestFailed {
            algorithm: "ML-DSA-44".to_string(),
            test_name: fp.test_name.to_string(),
            message: format!("pk length: expected {}, got {}", fp.pk_len, pk_bytes.len()),
        });
    }
    if pk_bytes[..32] != expected_pk_prefix[..] {
        return Err(NistKatError::TestFailed {
            algorithm: "ML-DSA-44".to_string(),
            test_name: fp.test_name.to_string(),
            message: format!(
                "pk prefix mismatch: expected {}, got {}",
                fp.pk_first32,
                hex(&pk_bytes[..32])
            ),
        });
    }

    // Sign/verify roundtrip with empty message
    let sig = sk
        .try_sign(&[], &[])
        .map_err(|e| NistKatError::ImplementationError(format!("sign: {:?}", e)))?;
    let pk_restored = ml_dsa_44::PublicKey::try_from_bytes(pk_bytes)
        .map_err(|e| NistKatError::ImplementationError(format!("pk deser: {:?}", e)))?;
    if !pk_restored.verify(&[], &sig, &[]) {
        return Err(NistKatError::TestFailed {
            algorithm: "ML-DSA-44".to_string(),
            test_name: fp.test_name.to_string(),
            message: "Sign/verify roundtrip failed".to_string(),
        });
    }

    // Sign/verify with "abc" message
    let sig_abc = sk
        .try_sign(b"abc", &[])
        .map_err(|e| NistKatError::ImplementationError(format!("sign abc: {:?}", e)))?;
    if !pk_restored.verify(b"abc", &sig_abc, &[]) {
        return Err(NistKatError::TestFailed {
            algorithm: "ML-DSA-44".to_string(),
            test_name: fp.test_name.to_string(),
            message: "Sign/verify roundtrip for 'abc' failed".to_string(),
        });
    }

    Ok(())
}

